#include "../includes/libft.h"

int	ft_isalnum(int a)
{
	if ('a' <= a && a <= 'z')
		return (1);
	else if ('A' <= a && a <= 'Z')
		return (1);
	else if ('0' <= a && a <= '9')
		return (1);
	else
		return (0);
}
